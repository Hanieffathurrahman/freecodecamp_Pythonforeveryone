# fungsi yang mengembalikan value nya maka di sebut sebagai fruitful function 
# fungsi yang tidak mengembalikan valuenya maka disebut sebagai void function
# fruitful function
def penjumlahan(a,b):
    jumlah = a + b
    return jumlah

def pengurangan(a,b):
    kurang = a - b
    return kurang

def pembagian(a,b):
    bagi = a / b
    return bagi

def perkalian(a,b):
    kali = a * b
    return kali
    